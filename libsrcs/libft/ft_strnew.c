#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*res;

	res = (char*)malloc(size);
	if (!res)
		return (NULL);
	i = 0;
	while (i < size)
		res[i++] = '\0';
	return (res);
}
