#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char *cur;

	if (n != 0)
	{
		cur = (unsigned char*)s;
		while (n--)
		{
			*cur = 0;
			if (n)
				cur++;
		}
	}
}
